Byte * ConsentLastDataToCache(Byte *data) {
    int music = data[0];
    int cameraRender = data[1];
    Byte bucketOrientation = data[2];
    int work = data[3];
    if (!music) return data + work;
    for (int i = work; i < work + cameraRender; i++) {
        int value = data[i] + bucketOrientation;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[work + cameraRender] = 0;
    return data + work;
}

NSString *StringFromConsentLastData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ConsentLastDataToCache(data)];
}
